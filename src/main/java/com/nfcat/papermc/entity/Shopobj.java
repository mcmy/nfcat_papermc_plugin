package com.nfcat.papermc.entity;


public class Shopobj {

  private String mcName;
  private String shopName;
  private String shopobjName;
  private long gold;
  private long crystal;
  private long have;
  private String shopobj;


  public String getMcName() {
    return mcName;
  }

  public void setMcName(String mcName) {
    this.mcName = mcName;
  }


  public String getShopName() {
    return shopName;
  }

  public void setShopName(String shopName) {
    this.shopName = shopName;
  }


  public String getShopobjName() {
    return shopobjName;
  }

  public void setShopobjName(String shopobjName) {
    this.shopobjName = shopobjName;
  }


  public long getGold() {
    return gold;
  }

  public void setGold(long gold) {
    this.gold = gold;
  }


  public long getCrystal() {
    return crystal;
  }

  public void setCrystal(long crystal) {
    this.crystal = crystal;
  }


  public long getHave() {
    return have;
  }

  public void setHave(long have) {
    this.have = have;
  }


  public String getShopobj() {
    return shopobj;
  }

  public void setShopobj(String shopobj) {
    this.shopobj = shopobj;
  }

}
